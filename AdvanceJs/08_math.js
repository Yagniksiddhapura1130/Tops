// ceil
// તે આપેલ સંખ્યા કરતા વધારે અથવા તેના સમાન, સૌથી નાનું પૂર્ણાંક મૂલ્ય આપે છે

console.log("ceil()", Math.ceil(12.3));

// trunc
// તે આપેલ સંખ્યાનો પૂર્ણાંક ભાગ પરત કરે છે.

console.log("trunc()", Math.trunc(12.9));
console.log("trunc()", Math.trunc(12.3));

// floor
// તે આપેલ સંખ્યા કરતા નીચું અથવા બરાબર, સૌથી મોટું પૂર્ણાંક મૂલ્ય આપે છે.

console.log("floor()", Math.floor(12.9));
console.log("floor()", Math.floor(54.1));

// max
// તે આપેલ સંખ્યાઓની મહત્તમ કિંમત પરત કરે છે.

console.log("max()", Math.max(1, 23, 3, 45, 5, 78, 75, 4, 5));

// min
// તે આપેલ સંખ્યાઓનું ન્યૂનતમ મૂલ્ય પરત કરે છે.

console.log("min()", Math.min(1, 23, 3, 45, 5, 78, 75, 4, 5));

// random
// તે 0 (સમાવિષ્ટ) અને 1 (વિશિષ્ટ) ની વચ્ચે રેન્ડમ નંબર આપે છે.

console.log("random : ", Math.random());

// round
// તે આપેલ સંખ્યાની નજીકના પૂર્ણાંક મૂલ્ય પરત કરે છે.

console.log("round : ", Math.round(10.4));
console.log("round : ", Math.round(10.5));
console.log("round : ", Math.round(10.6));





